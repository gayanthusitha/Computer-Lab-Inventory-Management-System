    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // Get Entered Username and Password
        String username = txtUserName.getText();
        String password = new String(txtPassword.getPassword());

        // Check Username and Password Field
        if(username.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter User name and Password");
        }
        else{
            // Authenticate the user using User Class
            User user = User.authenticateUser(username, password);

            this.dispose();
            // Clear text
            txtUserName.setText("");
            txtPassword.setText("");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed

        int Clear = JOptionPane.showConfirmDialog(null, "Do you want to Clear", "", JOptionPane.YES_NO_OPTION);
        if(Clear == 0){
            txtUserName.setText("");
            txtPassword.setText("");
        }

    }//GEN-LAST:event_btnClearActionPerformed

